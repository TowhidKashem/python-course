# if
if a != b:
    print("a is not equal to b")


# if else
if a == b:
    print("a is equal b")
else:
    print("a is not equal b")


# if elseif else
if a == b:
    print("a is equal b")
elif a > b:
    print("a is greater than b")
else:
    print("hmmm")


# shorthand if else
print("Yes") if 5 > 2 else print("No")


# ternary expression
name = 'Towhid' if age == 33 else 'Julia'
